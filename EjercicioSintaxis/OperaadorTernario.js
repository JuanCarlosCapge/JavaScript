/*Expresión 1 y 2.- Expresiones con valores definidos que pueden ejecutarse 
dependiendo de la condición.
Forma de uso
variable = expresion ? true_value : false_value;
Esto permite básicamente una asignación condicional a una variable
 en función de la evaluación de la expresión. Si es cierto, 
 entonces true_value se asigna a la variable, caso contrario,
  entonces false_value se asigna a la variable.*/

Ejemplos
var max = ( num1 > num2 ) ? num1 : num2 ;


